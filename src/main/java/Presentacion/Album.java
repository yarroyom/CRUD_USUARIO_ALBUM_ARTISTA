    private void CargarTabla(){    
        try {
            AlbumDAOJDBC peliculaJdbc = new AlbumDAOJDBC();
            List<AlbumDTO> arts = peliculaJdbc.Select();
            DefaultTableModel modeloTabla = (DefaultTableModel) Tabla1.getModel();
            modeloTabla.setRowCount(0);
            Object[] fila = new Object[3];    
            for(AlbumDTO artista: arts){  
                    fila[0] = artista.getId_Album();
                    fila[1] = artista.getCancion_p();
                    fila[2] = artista.getF_Genero_Musical();
                 
                    modeloTabla.addRow(fila);
            }   
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Hay un erro en cargar tabla! "+ ex);
        }
    }
    private void Registrar(){
      AlbumDAOJDBC artistajdbc = new AlbumDAOJDBC();
        AlbumDTO art = new AlbumDTO();
        try {
            
            art.setCancion_p(txtCancion.getText());
            art.setF_Genero_Musical(txtGenero.getText());
           
            artistajdbc.Insert(art);
            CargarTabla();
            limpiar();
           
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Hay un erro en Insertar! "+ex);
        }    
  
    
    }
    
    private void actualizar(){
        AlbumDAOJDBC artistajdbc = new AlbumDAOJDBC();
        AlbumDTO arts = new AlbumDTO();
        try {
            arts.setId_Album(Integer.parseInt(txtId.getText()));
            arts.setCancion_p(txtCancion.getText());
            arts.setF_Genero_Musical(txtGenero.getText());
           
            artistajdbc.update(arts);
            CargarTabla();
            limpiar();       
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(this, "Hay un erro en Actualizar! "+ex); 
        }    
    }
    

    public void eliminar(){
        AlbumDAOJDBC artistajdbc = new AlbumDAOJDBC();
        AlbumDTO artis = new AlbumDTO();
        
        try {
            artis.setId_Album(Integer.parseInt(txtId.getText()));
            artistajdbc.Delete(artis);
            CargarTabla();
        } catch (Exception e) {
        }
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
